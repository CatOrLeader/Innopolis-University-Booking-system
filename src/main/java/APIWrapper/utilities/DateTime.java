package APIWrapper.utilities;

public class DateTime {
    private int year;
    private int month;
    private int day;
    private int hours;
    private int minutes;
    private int seconds;

    public DateTime(int year, int month, int day, int hours, int minutes, int seconds) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }
}

